def get_prefix(event_id):
    """提取事件ID前缀"""
    parts = event_id.split('_')
    if parts[-1].isdigit():
        return '_'.join(parts[:-1])
    return event_id


def generate_expanded_text(event):
    """根据事件类型路由到对应的生成器"""
    prefix = get_prefix(event['id'])
    
    generators = {
        "treasure": generate_treasure_text,
        "final_res": generate_treasure_text,
        "mega_realm": generate_realm_explore_text,
        "ext_explore": generate_realm_explore_text,
        "last_terrain": generate_realm_explore_text,
        "mega_pet": generate_creature_text,
        "last_creature": generate_creature_text,
        "fortune": generate_fortune_special_text,
        "dream": generate_dream_text,
        "ext_reincarn": generate_dream_text,
        "mega_div": generate_divination_text,
        "ext_destiny": generate_destiny_text,
    }
    
    gen = generators.get(prefix, generate_treasure_text)
    return gen(event)


def main():
    input_path = Path(__file__).parent / "events" / "fortune.json"
    output_path = input_path
    
    with open(input_path, 'r', encoding='utf-8') as f:
        events = json.load(f)
    
    print(f"读取 {len(events)} 个Fortune事件")
    
    # 保留的精品手写事件（原始质量就很高的）
    KEEP_IDS = {"fortune_001", "fortune_003", "fortune_004", "fortune_007", "fortune_009"}
    
    updated = 0
    skipped = 0
    for event in events:
        if event['id'] in KEEP_IDS:
            skipped += 1
            continue
        event['expanded_text'] = generate_expanded_text(event)
        updated += 1
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(events, f, ensure_ascii=False, indent=2)
    
    print(f"已更新 {updated} 个事件 (保留 {skipped} 个已有高质量文案)")
    
    # 统计
    lengths = [len(e['expanded_text']) for e in events]
    print(f"文案长度: min={min(lengths)}, max={max(lengths)}, avg={sum(lengths)//len(lengths)}")
    
    # 检查重复
    texts = [e['expanded_text'] for e in events]
    unique = len(set(texts))
    print(f"唯一文案: {unique}/{len(texts)}")


if __name__ == '__main__':
    main()
