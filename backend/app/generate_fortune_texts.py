def pick_by_hash(event_id, choices):
    """基于event_id的hash确定性选择"""
    h = int(hashlib.md5(event_id.encode()).hexdigest(), 16)
    return choices[h % len(choices)]


def pick_by_hash_idx(event_id, salt, choices):
    """带盐值的hash选择"""
    h = int(hashlib.md5(f"{event_id}_{salt}".encode()).hexdigest(), 16)
    return choices[h % len(choices)]


def detect_item_type(text):
    """从text中检测物品类型"""
    if any(k in text for k in ["剑", "刀", "枪", "戟", "鞭"]):
        return "剑"
    if any(k in text for k in ["丹", "药"]):
        return "丹"
    if any(k in text for k in ["书", "籍", "卷", "经", "诀", "秘笈", "笔记"]):
        return "书"
    if any(k in text for k in ["矿", "石", "晶"]):
        return "矿"
    return "default"


def detect_result_type(text):
    """从text中检测探索结果（优先级从高到低）"""
    # 危险类优先级最高
    if any(k in text for k in ["差点", "差一点", "回不来", "迷失"]):
        return "危险"
    # 空手类第二
    if any(k in text for k in ["空手", "什么也没发现", "一无所获", "什么都没有"]):
        return "空手"
    # 同伴类
    if any(k in text for k in ["同行", "结伴", "探险者"]):
        return "同伴"
    # 秘密类
    if "秘密" in text or ("发现了一个" in text and "秘" in text):
        return "秘密"
    # 修炼类
    if any(k in text for k in ["修炼", "闭关", "打坐"]):
        return "修炼"
    # 收获类放最后
    if any(k in text for k in ["满载而归", "收获", "有所收获", "获得", "传承"]):
        return "收获"
    return "收获"


