def generate_keywords(event):
    """生成3-5个keywords"""
    prefix = event['id'].rsplit('_', 1)[0]
    base = KEYWORD_MAP.get(prefix, ["NPC", "互动"])[:2]
    
    # 从effects中提取关键词
    effects = event.get('effects', {})
    for eff_key, kw in EFFECT_KEYWORDS.items():
        if eff_key in effects and kw not in base:
            base.append(kw)
            if len(base) >= 4:
                break
    
    # 从text中提取场景关键词
    text = event.get('text', '')
    scene_kws = {
        "月下": "月夜", "论道": "论道", "闭关": "闭关", "剑": "剑道",
        "丹": "丹道", "阵": "阵法", "突破": "突破", "战": "战斗",
        "历练": "历练", "秘境": "秘境", "渡劫": "渡劫", "传承": "传承",
        "毒": "毒术", "受伤": "疗伤", "切磋": "切磋", "考核": "考核",
    }
    for trigger, kw in scene_kws.items():
        if trigger in text and kw not in base:
            base.append(kw)
            if len(base) >= 5:
                break
    
    return base[:5]


def pick_by_hash(event_id, choices):
    """基于event_id的hash确定性选择，保证可复现"""
    h = int(hashlib.md5(event_id.encode()).hexdigest(), 16)
    return choices[h % len(choices)]


def pick_by_hash_idx(event_id, salt, choices):
    """带盐值的hash选择，同一event可选多个不同元素"""
    h = int(hashlib.md5(f"{event_id}_{salt}".encode()).hexdigest(), 16)
    return choices[h % len(choices)]


