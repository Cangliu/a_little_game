def generate_expanded_text(event):
    """为单个NPC事件生成expanded_text"""
    eid = event['id']
    prefix = eid.rsplit('_', 1)[0]
    realm = event.get('conditions', {}).get('min_realm', 1)
    
    scenes, openings, closings = get_scene_lib(prefix)
    
    # 选择场景（主体内容）
    scene = pick_by_hash(eid, scenes)
    
    # 选择opening（70%概率使用，确保长度）
    h_open = int(hashlib.md5(f"{eid}_use_opening".encode()).hexdigest(), 16)
    use_opening = (h_open % 10 < 7)
    
    # 选择closing
    closing = pick_by_hash_idx(eid, "closing", closings)
    
    # 选择境界氛围修饰（40%概率嵌入）
    h_atm = int(hashlib.md5(f"{eid}_atmosphere".encode()).hexdigest(), 16)
    use_atmosphere = (h_atm % 5 < 2)
    
    # 选择扩展段（60%概率使用，确保足够长度）
    h_ext = int(hashlib.md5(f"{eid}_extension".encode()).hexdigest(), 16)
    use_extension = (h_ext % 5 < 3)
    extensions = SCENE_EXTENSIONS.get(prefix, SCENE_EXTENSIONS["npc_complex"])
    
    # 组装
    parts = []
    if use_opening:
        opening = pick_by_hash_idx(eid, "opening", openings)
        parts.append(opening)
    
    if use_atmosphere and realm in REALM_ATMOSPHERE:
        atm = pick_by_hash_idx(eid, "atm", REALM_ATMOSPHERE[realm])
        parts.append(atm + "——")
    
    parts.append(scene)
    
    if use_extension:
        ext = pick_by_hash_idx(eid, "ext", extensions)
        parts.append(ext)
    
    parts.append(closing)
    
    # 组合文本
    text = "".join(parts)
    
    # 如果仍然太短（<150），强制加入扩展段
    if len(text) < 150:
        ext = pick_by_hash_idx(eid, "ext_force", extensions)
        text = text[:-len(closing)] + ext + closing
    
    return text


def main():
    input_path = Path(__file__).parent / "events" / "npc_exclusive.json"
    output_path = input_path  # 直接覆盖
    
    with open(input_path, 'r', encoding='utf-8') as f:
        events = json.load(f)
    
    print(f"读取 {len(events)} 个NPC事件")
    
    updated = 0
    for event in events:
        # 生成 expanded_text
        event['expanded_text'] = generate_expanded_text(event)
        # 生成 keywords
        event['keywords'] = generate_keywords(event)
        updated += 1
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(events, f, ensure_ascii=False, indent=2)
    
    print(f"已更新 {updated} 个事件的 expanded_text 和 keywords")
    
    # 统计
    lengths = [len(e['expanded_text']) for e in events]
    print(f"文案长度: min={min(lengths)}, max={max(lengths)}, avg={sum(lengths)//len(lengths)}")


if __name__ == '__main__':
    main()
